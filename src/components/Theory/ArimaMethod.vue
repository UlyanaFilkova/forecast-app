      <p>
        Модель <b>ARIMA</b> (AutoRegressive Integrated Moving Average — автогрессивная
        интегрированная скользящая средняя) широко используется для анализа временных рядов и
        прогнозирования. Она объединяет три ключевых компонента:
      </p>
      <ul>
        <li>
          <b>AR (авторегрессия):</b> использует зависимости между текущим и прошлыми значениями.
        </li>
        <li>
          <b>I (интеграция):</b> процедура преобразования нестационарного ряда в стационарный
        </li>
        <li>
          <b>MA (скользящая средняя):</b> учитывает зависимость текущего значения от
          ошибок предсказания предыдущих значений.
        </li>
      </ul>

      
    </section>
    <section>
      <h2>AR (Авторегрессия)</h2>
      <p>
        <strong>Что это такое?</strong> Авторегрессия предполагает, что текущее значение временного ряда зависит от нескольких его предыдущих значений (лагов). Эта зависимость описывается уравнением, в котором используются коэффициенты для каждого лага.
      </p>
      <p>
        <strong>Формула AR(p):</strong>
        <mathjax>
          \[y = c + \phi_1 y_{t-1} + \phi_2 y_{t-2} + \dots + \phi_p y_{t-p} + \epsilon_t\]
        </mathjax>
      </p>
      <p>
        <strong>Объяснение параметров:</strong>
        <ul>
          <li><mathjax>\(y\)</mathjax>: текущее значение временного ряда.</li>
          <li><mathjax>\(\phi_1, \phi_2, \dots, \phi_p\)</mathjax>: коэффициенты авторегрессии.</li>
          <li><mathjax>\(p\)</mathjax>: количество учитываемых предыдущих значений (лагов).</li>
          <li><mathjax>\(c\)</mathjax>: константа.</li>
          <li><mathjax>\(\epsilon_t\)</mathjax>: случайная ошибка (шум).</li>
        </ul>
      </p>
      <p>
        Если <mathjax>\(\phi_1 > 0\)</mathjax>, то предыдущее значение положительно влияет на текущее. Если <mathjax>\(\phi_1 < 0\)</mathjax>, наблюдается обратная зависимость.
      </p>
      <p><strong>Пример: </strong>Если у вас есть данные о продажах за последние месяцы, вы можете использовать AR-модель, чтобы предсказать продажи в текущем месяце, основываясь на продажах в предыдущих двух месяцах (если взять p = 2).</p>
    </section>

    <section>
      <h2>I (Интеграция)</h2>
      <p>
        <strong>Что это такое?</strong> Интеграция используется для обработки нестационарных временных рядов. Нестационарный ряд — это ряд, у которого статистические свойства (например, среднее и дисперсия) изменяются со временем.
         
      </p>
      <p>Чтобы сделать временной ряд стационарным, часто используется операция разности: мы вычитаем предыдущее значение из текущего. </p>
      <p>
        <strong>Процедура интеграции:</strong>
        <mathjax>
          \[y' = y - y_{t-1}\]
        </mathjax>
        Если после одной операции ряд остаётся нестационарным, процесс повторяют:
        <mathjax>
          \[y'' = y' - y'_{t-1} = y - 2y_{t-1} + y_{t-2}\]
        </mathjax>
      </p>
      <p>
        Параметр <mathjax>\(d\)</mathjax> указывает, сколько раз нужно выполнить эту операцию, чтобы сделать ряд стационарным.
      </p>
      <p><strong>Пример: </strong>Если у вас есть данные о ценах на акции, которые имеют тренд (постепенное увеличение или уменьшение), вы можете использовать разности, чтобы убрать этот тренд и сделать ряд стационарным.</p>
    
    </section>

    <section>
      <h2>MA (Скользящая средняя)</h2>
      <p>
        <strong>Что это такое?</strong> Модель скользящей средней основывается на анализе ошибки предсказания, допущенной в предыдущие моменты времени. Она предполагает, что текущее значение зависит от ошибки прогнозов для нескольких прошлых значений.
      </p>
      <p>
        <strong>Формула MA(q):</strong>
        <mathjax>
          \[y = c + \epsilon_t + \theta_1 \epsilon_{t-1} + \theta_2 \epsilon_{t-2} + \dots + \theta_q \epsilon_{t-q}\]
        </mathjax>
      </p>
      <p>
        <strong>Объяснение параметров:</strong>
        <ul>
          <li><mathjax>\(y\)</mathjax>: текущее значение временного ряда.</li>
          <li><mathjax>\(\epsilon_t, \epsilon_{t-1}, \dots, \epsilon_{t-q}\)</mathjax>: ошибки прогнозов.</li>
          <li><mathjax>\(\theta_1, \theta_2, \dots, \theta_q\)</mathjax>: коэффициенты, описывающие влияние ошибок.</li>
          <li><mathjax>\(q\)</mathjax>: количество учитываемых ошибок.</li>
        </ul>
      </p>
      <p><strong>Пример: </strong>Если вы прогнозируете температуру на основе предыдущих ошибок в прогнозах, вы можете использовать MA-модель, чтобы учесть, как сильно ваши предыдущие прогнозы ошибались.</p>
    
    </section>

   
    <section>
        
      
      <h2>Математическая формулировка ARIMA</h2>
      <p>Модель ARIMA обозначается как \(ARIMA(p, d, q)\), где:</p>
      <ul>
        <li>\(p\) — порядок авторегрессии (число лагов в модели AR).</li>
        <li>\(d\) — порядок дифференцирования (число разностей для достижения стационарности).</li>
        <li>\(q\) — порядок скользящей средней (число лагов ошибок в модели MA).</li>
      </ul>
      <p>Формула модели ARIMA выглядит так:</p>
      <p class="formula">
        \( y_t = c + \phi_1 y_{t-1} + \phi_2 y_{t-2} + \dots + \phi_p y_{t-p} + \epsilon_t +
        \theta_1 \epsilon_{t-1} + \theta_2 \epsilon_{t-2} + \dots + \theta_q \epsilon_{t-q} \)
      </p>
      <p>Где:</p>
      <ul>
        <li>\(y_t\) — текущее значение временного ряда.</li>
        <li>\(c\) — константа.</li>
        <li>\(\phi\) — коэффициенты авторегрессии.</li>
        <li>\(\epsilon_t\) — ошибка в текущий момент времени.</li>
        <li>\(\theta\) — коэффициенты скользящей средней.</li>
      </ul>

      <h2>Применение модели ARIMA</h2>
      <ul>
        <li>Прогнозирование продаж и спроса.</li>
        <li>Финансовый анализ (например, прогнозирование цен акций).</li>
        <li>Экономические временные ряды (ВВП, инфляция и т.д.).</li>
        <li>Мониторинг производственных процессов.</li>
      </ul>

      <h2>Ограничения модели ARIMA</h2>
      <ul>
        <li>Требование стационарности временного ряда.</li>
        <li>Сложности с моделированием данных с сезонностью (для этого используется SARIMA).</li>
        <li>Не подходит для временных рядов с сильно нелинейными зависимостями.</li>
      </ul>

      <h2>Расширения модели ARIMA</h2>
      <ul>
        <li><b>SARIMA:</b> модель ARIMA с учётом сезонности.</li>
        <li><b>ARIMAX:</b> модель ARIMA с дополнительными объясняющими переменными.</li>
      </ul>
